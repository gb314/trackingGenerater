package test.test.tracking;

public class TrackingDataItemProperty {

    private String propNameDescription;

    private String name;

    private String propType;

    private String propTypeDescription;

    private String methodName;

    public String getPropNameDescription() {
        return propNameDescription;
    }

    public void setPropNameDescription(String propNameDescription) {
        this.propNameDescription = propNameDescription;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPropType() {
        return propType;
    }

    public void setPropType(String propType) {
        this.propType = propType;
    }

    public String getPropTypeDescription() {
        return propTypeDescription;
    }

    public void setPropTypeDescription(String propTypeDescription) {
        this.propTypeDescription = propTypeDescription;
    }

    public String getMethodName() {
        return methodName;
    }

    public void setMethodName(String methodName) {
        this.methodName = methodName;
    }
}
